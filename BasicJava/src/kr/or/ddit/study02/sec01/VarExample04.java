package kr.or.ddit.study02.sec01;

public class VarExample04 {
	public static void main(String[] args) {
		/*
		 * 1. 변수 x를 선언하고
		 * 2. 값 10을 저장
		 * 3. 저장된 값 출력
		 */
		int x;
		x = 10;

		int y=10;
		
		//z값을 선언 x+y 값을 저장
		int z = x+y;
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
	}

}
